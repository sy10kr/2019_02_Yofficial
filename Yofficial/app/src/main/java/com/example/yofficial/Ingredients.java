package com.example.yofficial;

import java.util.ArrayList;

public class Ingredients {

    ArrayList<String> meats;
    ArrayList<String> grains;
    ArrayList<String> begetables;
    ArrayList<String> fruits;
    ArrayList<String> seafoods;
    ArrayList<String> eggs;
    ArrayList<String> nuts;
    ArrayList<String> noodles;
    ArrayList<String> sauces;
    ArrayList<String> oils;


    public ArrayList<String> getMeats() {
        return meats;
    }

    public void setMeats(ArrayList<String> meats) {
        this.meats = meats;
    }

    public ArrayList<String> getGrains() {
        return grains;
    }

    public void setGrains(ArrayList<String> grains) {
        this.grains = grains;
    }

    public ArrayList<String> getBegetables() {
        return begetables;
    }

    public void setBegetables(ArrayList<String> begetables) {
        this.begetables = begetables;
    }

    public ArrayList<String> getFruits() {
        return fruits;
    }

    public void setFruits(ArrayList<String> fruits) {
        this.fruits = fruits;
    }

    public ArrayList<String> getSeafoods() {
        return seafoods;
    }

    public void setSeafoods(ArrayList<String> seafoods) {
        this.seafoods = seafoods;
    }

    public ArrayList<String> getEggs() {
        return eggs;
    }

    public void setEggs(ArrayList<String> eggs) {
        this.eggs = eggs;
    }

    public ArrayList<String> getNuts() {
        return nuts;
    }

    public void setNuts(ArrayList<String> nuts) {
        this.nuts = nuts;
    }

    public ArrayList<String> getNoodles() {
        return noodles;
    }

    public void setNoodles(ArrayList<String> noodles) {
        this.noodles = noodles;
    }

    public ArrayList<String> getSauces() {
        return sauces;
    }

    public void setSauces(ArrayList<String> sauces) {
        this.sauces = sauces;
    }

    public ArrayList<String> getOils() {
        return oils;
    }

    public void setOils(ArrayList<String> oils) {
        this.oils = oils;
    }
}
